// playClintEastwood plays one loop of the Clint Eastwood ASCII animation in red.
func playClintEastwood(ctx context.Context, w io.Writer) {
	if f, ok := w.(*os.File); !ok || !isatty.IsTerminal(f.Fd()) {
		return
	}
	fmt.Fprint(w, ansiHide)
	defer fmt.Fprint(w, ansiShow)
	for _, frame := range clintEastwoodFrames {
		select {
		case <-ctx.Done():
			return
		default:
		}
		fmt.Fprint(w, ansiClear, ansiRed, frame, ansiReset)
		time.Sleep(150 * time.Millisecond)
	}
}
